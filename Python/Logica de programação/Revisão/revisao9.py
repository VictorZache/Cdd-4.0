#faça um codigo  que receba um numero inteiro e mostre o seu antecessor


numero=int(input("Digite um numero para saber qual o seu antecessor e sucessor:"))

numeroAnt= numero - 1
numeroSu= numero + 1

print(f'O numero antecessor de {numero} é {numeroAnt}.')
print(f'O numero sucessor de {numero} é {numeroSu}.')